class Comparision
{
	public static void main(String[] args) 
	{
		System.out.println();

		int x1=20;
		int x2=10;

		System.out.println(x1 < x2);
		System.out.println(x1 > x2);
		System.out.println(x1 == x2);
		System.out.println(x1 != x2);
		System.out.println(x1 <= x2);
		System.out.println(x1 >= x2);


		System.out.println();
		
	}
}